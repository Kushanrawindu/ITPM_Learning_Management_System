    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $this->validate($request, [
            'subName'=>'required',
            'subFile'=>'required|mimes:docx,pdf,ppt,rar,zip|max:1999'
        ]);

        $course = new course();
        $course->year = $request->academic_id;
        $course->semester = $request->semester_id;
        $course->batch = $request->batch_id;
        $course->intake = $request->intake_id;
        $course->course = $request->course_id;
        $course->degree = $request->degree_id;
        $course->date = $request->date;
        $course->save();
        return redirect(route('course.index'));
    }

